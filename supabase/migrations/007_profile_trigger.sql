-- 007_profile_trigger.sql — auto-create a public.profiles row whenever a new
-- auth.users row is created (signup). Without this, a freshly signed-up user
-- has no profile row and every "profiles own" RLS query returns nothing.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, display_name)
  values (new.id, coalesce(new.raw_user_meta_data->>'display_name', ''))
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();
